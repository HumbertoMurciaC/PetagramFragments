package com.example.humbertomariom.petagram;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;

/**
 * Created by Humberto Mario M on 3/07/2016.
 */
public class PageAdaptador extends FragmentPagerAdapter {

    ArrayList<Fragment> fragments;

    public PageAdaptador(FragmentManager fm, ArrayList<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}